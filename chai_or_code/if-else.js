//  find a day use switch case

let day = "mon";
switch (day) {
  case "sun":
    console.log("Sunday");
    break;
  case "mon":
    console.log("Monday");
    break;
  case "tue":
    console.log("Tuesday");
    break;
  case "wed":
    console.log("Wednesday");
    break;
  case "thu":
    console.log("Thusrsday");
    break;
  case "fri":
    console.log("Friday");
    break;
  case "sat":
    console.log("Saturday");

  default:
    break;
}

